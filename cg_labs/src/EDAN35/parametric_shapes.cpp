bonobo::mesh_data
parametric_shapes::createQuad(float const width, float const height,
	unsigned int const horizontal_split_count,
	unsigned int const vertical_split_count)
{
	auto const vertices_nb = (horizontal_split_count + 1) * (vertical_split_count + 1);

	auto vertices = std::vector<glm::vec3>(vertices_nb);
	auto normals = std::vector<glm::vec3>(vertices_nb);
	auto texcoords = std::vector<glm::vec3>(vertices_nb);
	auto tangents = std::vector<glm::vec3>(vertices_nb);
	auto binormals = std::vector<glm::vec3>(vertices_nb);

	auto const delta_vertical = height / static_cast<float>(vertical_split_count);
	auto const delta_horizontal = width / static_cast<float>(horizontal_split_count);

	unsigned index = 0u;
	for (unsigned z = 0; z < horizontal_split_count + 1; ++z) {
		float z_pos = delta_vertical * z;
		for (unsigned x = 0; x < vertical_split_count + 1; ++x) {
			float x_pos = delta_horizontal * x;
			index = z * (horizontal_split_count + 1) + x;

			vertices[index] = glm::vec3(x_pos, 0.0f, z_pos);

			tangents[index] = glm::vec3(1.0f, 0.0f, 0.0f);
			binormals[index] = glm::vec3(0.0f, 0.0f, 1.0f);
			normals[index] = glm::vec3(0.0f, 1.0f, 0.0f);

			texcoords[index] = glm::vec3(static_cast<float>(z) / (static_cast<float>(horizontal_split_count)),
				static_cast<float>(x) / (static_cast<float>(vertical_split_count)),
				0.0f);

		}
	}
	auto index_sets = std::vector<glm::uvec3>(2 * horizontal_split_count * vertical_split_count);

	auto get_vertex_idx = [&vertical_split_count](auto x, auto z) {
		return z * (vertical_split_count + 1) + x;
		};


	index = 0u;
	for (unsigned z = 0; z < horizontal_split_count; ++z) {
		for (unsigned x = 0; x < vertical_split_count; ++x) {
			index_sets[index] = glm::uvec3(get_vertex_idx(x, z), get_vertex_idx(x + 1, z + 1), get_vertex_idx(x, z + 1));
			++index;

			index_sets[index] = glm::uvec3(get_vertex_idx(x, z), get_vertex_idx(x + 1, z), get_vertex_idx(x + 1, z + 1));
			++index;
		}
	}

	bonobo::mesh_data data;
	if (horizontal_split_count < 1 || vertical_split_count < 1) {
		LogError("Quad can't have less than 2 splits");
		return data;
	}



	glGenVertexArrays(1, &data.vao);
	glBindVertexArray(data.vao);


	auto const vertices_offset = 0u;
	auto const vertices_size = static_cast<GLsizeiptr>(vertices.size() * sizeof(glm::vec3));
	auto const normals_offset = vertices_size;
	auto const normals_size = static_cast<GLsizeiptr>(normals.size() * sizeof(glm::vec3));
	auto const texcoords_offset = normals_offset + normals_size;
	auto const texcoords_size = static_cast<GLsizeiptr>(texcoords.size() * sizeof(glm::vec3));
	auto const tangents_offset = texcoords_offset + texcoords_size;
	auto const tangents_size = static_cast<GLsizeiptr>(tangents.size() * sizeof(glm::vec3));
	auto const binormals_offset = tangents_offset + tangents_size;
	auto const binormals_size = static_cast<GLsizeiptr>(binormals.size() * sizeof(glm::vec3));
	auto const bo_size = static_cast<GLsizeiptr>(
		vertices_size
		+ normals_size
		+ texcoords_size
		+ tangents_size
		+ binormals_size
		);

	glGenBuffers(1, &data.bo);
	assert(data.bo != 0u);
	glBindBuffer(GL_ARRAY_BUFFER, data.bo);
	glBufferData(GL_ARRAY_BUFFER, bo_size, nullptr, GL_STATIC_DRAW);



	glBufferSubData(GL_ARRAY_BUFFER, vertices_offset, vertices_size, static_cast<GLvoid const*>(vertices.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::vertices));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::vertices), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(0x0));

	glBufferSubData(GL_ARRAY_BUFFER, normals_offset, normals_size, static_cast<GLvoid const*>(normals.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::normals));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::normals), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(normals_offset));

	glBufferSubData(GL_ARRAY_BUFFER, texcoords_offset, texcoords_size, static_cast<GLvoid const*>(texcoords.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::texcoords));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::texcoords), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(texcoords_offset));

	glBufferSubData(GL_ARRAY_BUFFER, tangents_offset, tangents_size, static_cast<GLvoid const*>(tangents.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::tangents));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::tangents), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(tangents_offset));

	glBufferSubData(GL_ARRAY_BUFFER, binormals_offset, binormals_size, static_cast<GLvoid const*>(binormals.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::binormals));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::binormals), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(binormals_offset));


	glBindBuffer(GL_ARRAY_BUFFER, 0u);

	glGenBuffers(1, &data.ibo);
	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, data.ibo);

	glBufferData(GL_ELEMENT_ARRAY_BUFFER, index_sets.size() * sizeof(glm::uvec3), index_sets.data(), GL_STATIC_DRAW);

	data.indices_nb = index_sets.size() * 3u;

	glBindVertexArray(0u);
	glBindBuffer(GL_ARRAY_BUFFER, 0u);
	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, 0u);

	return data;
}

bonobo::mesh_data
parametric_shapes::createSphere(float const radius,
	unsigned int const longitude_split_count,
	unsigned int const latitude_split_count)
{

	auto const vertices_nb = longitude_split_count * latitude_split_count;

	auto vertices = std::vector<glm::vec3>(vertices_nb);
	auto normals = std::vector<glm::vec3>(vertices_nb);
	auto texcoords = std::vector<glm::vec3>(vertices_nb);
	auto tangents = std::vector<glm::vec3>(vertices_nb);
	auto binormals = std::vector<glm::vec3>(vertices_nb);

	float const d_theta = glm::two_pi<float>() / (static_cast<float>(longitude_split_count - 1));
	float const d_phi = glm::pi<float>() / (static_cast<float>(latitude_split_count - 1));

	auto vertex_idx = [&longitude_split_count, &latitude_split_count](int i, int j) {
		return (i % longitude_split_count) + j * longitude_split_count;
		};


	auto index = 0u;
	for (int i = 0; i < longitude_split_count; ++i) {
		float sin_theta = std::sin(d_theta * i);
		float cos_theta = std::cos(d_theta * i);
		for (int j = 0; j < latitude_split_count; ++j) {
			float sin_phi = std::sin(d_phi * j);
			float cos_phi = std::cos(d_phi * j);


			index = j * longitude_split_count + i;

			auto pos = glm::vec3(
				radius * sin_theta * sin_phi,
				-radius * cos_phi,
				radius * cos_theta * sin_phi
			);
			vertices[index] = pos;


			tangents[index] = glm::normalize(glm::vec3(
				cos_theta,
				0,
				-sin_theta
			));

			binormals[index] = glm::normalize(glm::vec3(
				sin_theta * cos_phi,
				sin_phi,
				cos_theta * cos_phi
			));

			normals[index] = glm::cross(tangents[index], binormals[index]);

			texcoords[index] = glm::vec3(static_cast<float>(i) / (static_cast<float>(longitude_split_count)),
				static_cast<float>(j) / (static_cast<float>(latitude_split_count)),
				0.0f);
		}
	}

	auto index_sets = std::vector<glm::uvec3>(2u * longitude_split_count * latitude_split_count);


	index = 0u;
	for (int i = 0; i < longitude_split_count - 1; ++i) {
		for (int j = 0; j < latitude_split_count - 1; ++j) {

			index_sets[index] = glm::uvec3(vertex_idx(i, j), vertex_idx(i + 1, j), vertex_idx(i + 1, j + 1));
			++index;
			index_sets[index] = glm::uvec3(vertex_idx(i, j), vertex_idx(i + 1, j + 1), vertex_idx(i, j + 1));
			++index;
		}
	}



	bonobo::mesh_data data;
	glGenVertexArrays(1, &data.vao);
	assert(data.vao != 0u);
	glBindVertexArray(data.vao);

	auto const vertices_offset = 0u;
	auto const vertices_size = static_cast<GLsizeiptr>(vertices.size() * sizeof(glm::vec3));
	auto const normals_offset = vertices_size;
	auto const normals_size = static_cast<GLsizeiptr>(normals.size() * sizeof(glm::vec3));
	auto const texcoords_offset = normals_offset + normals_size;
	auto const texcoords_size = static_cast<GLsizeiptr>(texcoords.size() * sizeof(glm::vec3));
	auto const tangents_offset = texcoords_offset + texcoords_size;
	auto const tangents_size = static_cast<GLsizeiptr>(tangents.size() * sizeof(glm::vec3));
	auto const binormals_offset = tangents_offset + tangents_size;
	auto const binormals_size = static_cast<GLsizeiptr>(binormals.size() * sizeof(glm::vec3));
	auto const bo_size = static_cast<GLsizeiptr>(vertices_size
		+ normals_size
		+ texcoords_size
		+ tangents_size
		+ binormals_size
		);
	glGenBuffers(1, &data.bo);
	assert(data.bo != 0u);
	glBindBuffer(GL_ARRAY_BUFFER, data.bo);
	glBufferData(GL_ARRAY_BUFFER, bo_size, nullptr, GL_STATIC_DRAW);

	glBufferSubData(GL_ARRAY_BUFFER, vertices_offset, vertices_size, static_cast<GLvoid const*>(vertices.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::vertices));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::vertices), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(0x0));

	glBufferSubData(GL_ARRAY_BUFFER, normals_offset, normals_size, static_cast<GLvoid const*>(normals.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::normals));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::normals), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(normals_offset));

	glBufferSubData(GL_ARRAY_BUFFER, texcoords_offset, texcoords_size, static_cast<GLvoid const*>(texcoords.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::texcoords));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::texcoords), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(texcoords_offset));

	glBufferSubData(GL_ARRAY_BUFFER, tangents_offset, tangents_size, static_cast<GLvoid const*>(tangents.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::tangents));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::tangents), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(tangents_offset));

	glBufferSubData(GL_ARRAY_BUFFER, binormals_offset, binormals_size, static_cast<GLvoid const*>(binormals.data()));
	glEnableVertexAttribArray(static_cast<unsigned int>(bonobo::shader_bindings::binormals));
	glVertexAttribPointer(static_cast<unsigned int>(bonobo::shader_bindings::binormals), 3, GL_FLOAT, GL_FALSE, 0, reinterpret_cast<GLvoid const*>(binormals_offset));

	glBindBuffer(GL_ARRAY_BUFFER, 0u);

	data.indices_nb = static_cast<GLsizei>(index_sets.size() * 3u);

	glGenBuffers(1, &data.ibo);
	assert(data.ibo != 0u);
	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, data.ibo);

	glBufferData(GL_ELEMENT_ARRAY_BUFFER, static_cast<GLsizeiptr>(index_sets.size() * sizeof(glm::uvec3)), reinterpret_cast<GLvoid const*>(index_sets.data()), GL_STATIC_DRAW);

	glBindVertexArray(0u);
	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, 0u);

	return data;
}

